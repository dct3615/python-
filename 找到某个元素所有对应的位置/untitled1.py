# =============================================================================
# 使用list的index方法可以找到list中第一次出现该元素的位置
# =============================================================================
l = ['a','b','c','c','d','c']
find='b'
print(l.index(find))

# =============================================================================
# 找出出现该元素的所有位置可以使用一个简单的表理解来实现
# =============================================================================
find = 'c'
print([i for i,v in enumerate(l) if v==find])
